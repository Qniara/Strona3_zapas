    title VARCHAR(255),
    band VARCHAR(255)
);
CREATE TABLE REM (
    id INT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(255),
    band VARCHAR(255)
);
CREATE TABLE GunsNRoses (
